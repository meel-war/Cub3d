#include "../libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	s;

	if (!dst || !src)
		return (-1);
	i = 0;
	s = 0;
	while (src[s])
		s++;
	if (size <= 0)
		return (s);
	while (src[i] != '\0' && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (s);
}
